	 <h4> <b> Tari Pagar Pengantin </b> </h4> 
	<p> Tarian ini berasal dari Kota Palembang, Provinsi Sumatra Selatan, Indonesia.
Tarian ini biasanya ditampilkan saat resepsi pernikahan walaupun terkadang ada yang
menggunakan Tari Gending Sriwijaya dalam penikahan adat Palembang. Dua tarian yang berbeda
Tari Pagar Pengantin memiliki arti khusus, yaitu melambangkan perpisahan pengantin perempuan
dari masa remajanya, melepas masa lajang,  berpisah dengan teman-teman sepermainan serta tarian 
terakhir bagi mempelai puteri karena setelah itu tidak diperkenankan lagi menari di depan umum
kecuali atas izin suami. Tindakan-tindakannya tak bebas lagi. Ia sudah berada di lingkaran kehidupan
rumah tangga yang direpresentasikan dengan dulang agung keemasan. </p>

<p> Mempelai puteri menari diatas nampan atau dulang berlapis emas yang berisi bunga berwarna warni.
Nampan tersebut mempunyai makna sebagai bunga teratai yang tidak tenggelam akan pasang surut gelombang.
Mempelai puteri dipakaikan tanggai dijari jemarinya agar jarinya lentik saat menari.
Ini merupakan lambang kebersamaan seperti jari jemari ditangan kita. </p>

<p> Mempelai wanita menari diiringi oleh 4 orang penari pengiring yang biasanya adalah saudari perempuan lainnya.
Namun saat ini posisi para penari pengiring digantikan oleh penari-penari profesional.
Tarian ini dilakukan didepan mempelai pria dan menggambarkan bahwa pengantin wanita tersebut hanya akan bertindak
didalam dulang yang menunjukan bahwa wanita pengantin sekarang ruang geraknya terbatas karena sudah menikah.
Hal ini mengambarkan bahwa sang pria siap menjaga sang istri. </p>

</p> <br>

