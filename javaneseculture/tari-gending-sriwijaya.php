	<h4> <b> Tari Gending Sriwijaya </b> </h4>
	<p> Gending Sriwijaya merupakan lagu daerah dan juga tarian yang cukup populer dari kota Palembang Sumatera Selatan. Lagu Gending Sriwijaya ini dibawakan untuk mengiringi tari Gending Sriwijaya. Baik lagu maupun tarian ini menggambarkan keluhuran budaya, kejayaan, dan keagungan kemaharajaan Sriwijaya yang pernah berjaya mempersatukan wilayah Barat Nusantara Lirik lagu ini juga menggambarkan kerinduan seseorang akan zaman di mana pada saat itu Sriwijaya pernah menjadi pusat studi agama Buddha di dunia.
Tari Gending Sriwijaya dari Sumatera Selatan ini dibawakan untuk menyambut tamu-tamu agung. Biasanya tarian ini dibawakan oleh sebanyak 13 orang penari, yang terdiri dari 9 orang penari inti dan 4 orang pendamping dan penyanyi :
<ul>
<li> Satu orang penari utama pembawa tepak (tepak, kapur, sirih) </li>
<li> Dua orang penari pembawa peridon (perlengkapan tepak) </li>
<li> Enam orang penari pendamping (tiga dikanan dan tiga kiri) </li>
<li> Satu orang pembawa payung kebesaran (dibawa oleh pria) </li>
<li> Satu orang penyanyi Gending Sriwijaya </li>
<li> Dua orang pembawa tombak (pria) </li>
</ul> <br> 
<p> Namun saat ini  penyanti gending sriwijaya sudah banyak digantikan dengan media digital dan elektronik seperti VCD maupun tape recorder. </p> <br>
