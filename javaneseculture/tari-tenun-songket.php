	 <h4> <b> Tari Tenun Songket </b> </h4> 
	<p> Tradisi menenun di masyarakat Palembang, Sumatera Selatan, sudah ada sejak zaman kerajaan Sriwijaya.
Hasil tenun songket Palembang terkenal memiliki corak yang indah dengan menggunakan benang yang dilapisi emas.
Tradisi menenun dahulu kerap dilakukan oleh perempuan Palembang.  Sementara, hasil kain tenun menjadi bahan baku
utama dalam pembuatan pakaian adat perkawinan yang dikenakan oleh mempelai perempuan.</p>

<p> Terinspirasi dari tradisi menenun itulah kemudian lahir sebuah garapan tari kreasi yang berjudul tari rampak kipas songket brada.
Tarian ini menceritakan tentang ketekunan dan kegembiraan para gadis Palembang dalam kegiatan menenun songket.
Secara umum, tari rampak kipas songket brada merupakan tari kreasi yang ditarikan oleh lima orang penari.
Jumlah tersebut bukanlah aturan baku dalam tarian, sehingga jumlah penari bisa ditambah dan dikurangi sesuai dengan besar kecilnya panggung yang digunakan. </p>

<p> Dari garapan kostum, tari rampak kipas songket brada menggunakan baju kurung khas Palembang yang telah dimodifikasi.
Meski telah dimodifikasi, ciri khas sebagai baju adat khas Palembang tidak hilang. Hal tersebut terlihat dari warna emas
yang mendominasi warna pakaian, selain juga penggunaan kain songket di bagian bawahnya.
Sementara bagian kepala penari dihias dengan mahkota bunga serupa kembang goyang. Tidak lupa kipas yang digunakan
sebagai properti terpenting dalam pementasan tarian ini.
Gerakan tari rampak kipas songket brada didominasi oleh gerakan tangan. Gerakan tangan tersebut menggambarkan para gadis Palembang
yang bergembira dalam tradisi menenun membuat kain songket. Di bagian akhir pementasan, para penari akan mengeluarkan kipas sebagai
ciri utama dalam pementasan tarian ini.</p> <br>

